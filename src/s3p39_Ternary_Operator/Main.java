package s3p39_Ternary_Operator;

public class Main {

    public static void main(String[] args) {
        boolean isCar = false;

        /* If isCar is true, set wasCar to true, otherwise set isCar to false.  */
        boolean wasCar = isCar ? true : false;


        if (wasCar) {
            System.out.println("wasCar is true.  ");
        }
    }

}
